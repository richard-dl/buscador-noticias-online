// Helper para obtener todos los canales en un array plano
export const getAllChannels = () => {
  return Object.entries(channels).flatMap(([category, channelList]) =>
    channelList.map(channel => ({ ...channel, category }))
  );
};

// Helper para obtener las categorías
export const getCategories = () => Object.keys(channels);

// Helper para buscar canales
export const searchChannels = (query) => {
  const lowerQuery = query.toLowerCase();
  return getAllChannels().filter(channel =>
    channel.name.toLowerCase().includes(lowerQuery) ||
    channel.category.toLowerCase().includes(lowerQuery)
  );
};
